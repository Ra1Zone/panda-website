const dicts: Record<Lang, Dict> = { en, ar, he };

registerHebrewTranslations(
  Object.fromEntries(
    Object.keys(en)
      .filter((key) => he[key])
      .map((key) => [en[key], he[key]])
  )
);

function detectDeviceLang(): Lang {
  const langs = navigator.languages?.length ? navigator.languages : [navigator.language];
  for (const l of langs) {
    const code = l.toLowerCase();
    if (code.startsWith("ar")) return "ar";
    if (code.startsWith("he") || code.startsWith("iw")) return "he";
  }
  return "en";
}

interface I18nCtx {
  lang: Lang;
  dir: "ltr" | "rtl";
  t: (k: string) => string;
  setLang: (l: Lang) => void;
  toggle: () => void;
}

const Ctx = createContext<I18nCtx | null>(null);

export const I18nProvider = ({ children }: { children: ReactNode }) => {
  const [lang, setLangState] = useState<Lang>("en");
  const [translationVersion, setTranslationVersion] = useState(0);

  useEffect(() => {
    const saved = localStorage.getItem("lang") as Lang | null;
    const resolved = (saved === "ar" || saved === "en" || saved === "he") ? saved : detectDeviceLang();
    setLangState(resolved);
  }, []);

  const dir: "ltr" | "rtl" = lang === "ar" || lang === "he" ? "rtl" : "ltr";

  const setLang = (l: Lang) => {
    setLangState(l);
    localStorage.setItem("lang", l);
  };

  useEffect(() => {
    document.documentElement.lang = lang;
    document.documentElement.dir = dir;
  }, [lang, dir]);

  useEffect(() => subscribeToTranslationUpdates(() => {
    setTranslationVersion((version) => version + 1);
  }), []);

  const value = useMemo<I18nCtx>(() => ({
    lang,
    dir,
    t: (k) => {
      if (lang === "he") return dicts.he[k] ?? translateToHebrew(en[k] ?? k);
      return dicts[lang][k] ?? en[k] ?? k;
    },
    setLang,
    toggle: () => {
      const next: Lang = lang === "en" ? "ar" : lang === "ar" ? "he" : "en";
      setLang(next);
    },
  }), [lang, dir, translationVersion]);

  return <Ctx.Provider value={value}>{children}</Ctx.Provider>;
};

export const useI18n = () => {
  const c = useContext(Ctx);
  if (!c) throw new Error("useI18n outside provider");
  return c;
};
